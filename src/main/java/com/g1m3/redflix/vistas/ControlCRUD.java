    private void Button_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_ActualizarActionPerformed

        long w = Long.parseLong(ID_variable_P.getText());
        Optional<Peliculas> consulta3 = RepoP.findById(w);
        Optional<Peliculas> consulta4 = RepoP.findById(w);
        if (consulta3.isPresent()) {

            Peliculas pl2 = consulta3.get();
            Peliculas pl3 = consulta4.get();

            pl2.setTitulo_contenido(Titulo_Variable_P.getText());//almacenado tipo 
            pl2.setPel_resumen(Resumen_variable_p.getText());
            pl2.setPel_fecha(Integer.parseInt(year_variable_p.getText()));
            pl2.setId_director(Integer.parseInt(ID_var_dir_p.getText()));
            pl2.setPel_nom_director(Dir_var_p.getText());
            
            SALIDA.setText("\nSe Actualizaron los siguientes datos\n\n\n"
                    
                    +"Antes:\n"
                    + "ID pelicula: "+pl3.getId_pelicula()+"\n"
                    +"Titulo Pelicula: "+ pl3.getTitulo_contenido()+"\n"
                    +"Resumen: "+ pl3.getPel_resumen()+"\n"
                    +"Año: " + pl3.getPel_fecha()+"\n"
                    +"ID director: " + pl3.getId_director()+"\n"
                    +"Director: " + pl3.getPel_nom_director()+"\n"
                    
                    +"\n\nAhora:\n"
                    + "ID pelicula: "+pl2.getId_pelicula()+"\n"
                    +"Titulo Pelicula: "+ pl2.getTitulo_contenido()+"\n"
                    +"Resumen: "+ pl2.getPel_resumen()+"\n"
                    +"Año: " + pl2.getPel_fecha()+"\n"
                    +"ID director: " + pl2.getId_director()+"\n"
                    +"Director: " + pl2.getPel_nom_director()+"\n");
            
            RepoP.save(pl2);

        }else {
             SALIDA.setText("No se Encuentra la pelicula Con el ID: " + w);

        }

    }//GEN-LAST:event_Button_ActualizarActionPerformed

