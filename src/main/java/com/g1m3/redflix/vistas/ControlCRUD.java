    private void Button_BorradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_BorradoActionPerformed
        
        long y = Long.parseLong(ID_buscador_P.getText());
        Optional<Peliculas> consulta2 = RepoP.findById(y);
        if (consulta2.isPresent()) {
            
            Peliculas llamado2 = consulta2.get();// llama la consulta y lo guarda en un tipo contendio
            
            SALIDA.setText("Se Borro la pelicula: "+llamado2.getTitulo_contenido());
            
            RepoP.delete(llamado2);
            
            
        } else {
             SALIDA.setText("No se Encuentra la pelicula Con el ID: " + y);

        }
        
    }//GEN-LAST:event_Button_BorradoActionPerformed

